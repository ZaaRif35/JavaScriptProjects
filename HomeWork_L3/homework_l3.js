function calculArieDreptunghi(latime, lungime) {
    return latime * lungime;
}

let latimeDreptunghi = 5;
let lungimeDreptunghi = 10;
let arieDreptunghi = calculArieDreptunghi(latimeDreptunghi, lungimeDreptunghi);

console.log("Aria dreptunghiului cu lățimea", latimeDreptunghi, "și lungimea", lungimeDreptunghi, "este:", arieDreptunghi);
